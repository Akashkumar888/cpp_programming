#include<iostream>
using namespace std;
int main(){
    char ch='A';
    int y=(int)ch;
    cout<<"A = "<<y<<endl;
    char ch1='a';
    int z=(int)ch1;
    cout<<"a = "<<z<<endl;
    char ch2='0';
    int x=(int)ch2;
    cout<<"0 = "<<x<<endl;
    char ch3='#';
    int k=(int)ch3;
    cout<<"# = "<<k;
}